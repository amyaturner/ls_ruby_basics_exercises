# Q: Locate the documentation for break, and determine what value break sets the return value to for the while loop.

# A: "break accepts a value that supplies the result of the expression it is “breaking” out of"
# In other words, if you use break in a while loop, the while return value changes from nil to whatever value is returned
# from the break statement.


# Provided answer:
# break returns nil when no arguments are passed to break, or the value of break's argument when an argument is provided.